/**
 * Prisma 6 may generate .prisma/client without default.js while @prisma/client/default.js
 * still does require('.prisma/client/default'). Create the shim when missing.
 */
const fs = require("node:fs");
const path = require("node:path");

const clientDir = path.join(__dirname, "..", "node_modules", ".prisma", "client");
const indexJs = path.join(clientDir, "index.js");
const defaultJs = path.join(clientDir, "default.js");
const defaultDts = path.join(clientDir, "default.d.ts");

if (!fs.existsSync(indexJs)) {
  process.exit(0);
}

if (!fs.existsSync(defaultJs)) {
  fs.writeFileSync(
    defaultJs,
    "// Auto-generated by scripts/ensure-prisma-client-default.cjs\nmodule.exports = require('./index.js');\n",
    "utf8",
  );
}

if (!fs.existsSync(defaultDts)) {
  fs.writeFileSync(
    defaultDts,
    "// Auto-generated by scripts/ensure-prisma-client-default.cjs\nexport * from './index';\n",
    "utf8",
  );
}
