function cohortMetrics(map: Record<string, [number, number, number, number]>) {
  return Object.entries(map).map(([key, [p25, avg, p75, sample]]) => ({
    key,
    bottomQuartile: p25,
    industryAverage: avg,
    topQuartile: p75,
    sampleSize: sample * 100,
  }));
}

export function selectCohortForBusinessType(businessType: string | null | undefined): CohortSeed {
  const normalized = (businessType ?? "OTHER").toUpperCase();
  return (
    BENCHMARK_COHORTS.find((c) => c.businessType === normalized) ??
    BENCHMARK_COHORTS.find((c) => c.id === "all-na")!
  );
}

export function listBenchmarkCohorts(): CohortSeed[] {
  return BENCHMARK_COHORTS;
}
