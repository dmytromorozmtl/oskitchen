export const CHANNEL_REGISTRY_ENTRIES: readonly ChannelRegistryEntry[] =
  CHANNEL_DEFINITIONS.map((d) => enrichChannelRegistryEntry(d));

export const LIVE_CAPABLE_INTEGRATION_PROVIDERS: readonly IntegrationProvider[] = CHANNEL_DEFINITIONS.flatMap((def) =>
  def.mapsToIntegrationProvider && def.supportsLiveMode && !def.isPlaceholder ? [def.mapsToIntegrationProvider] : [],
);

export function providerCountsTowardLiveReadiness(provider: IntegrationProvider): boolean {
  return LIVE_CAPABLE_INTEGRATION_PROVIDERS.includes(provider);
}

export function channelByKey(key: string): ChannelDefinition | undefined {
  return CHANNEL_DEFINITIONS.find((c) => c.providerKey === key);
}

